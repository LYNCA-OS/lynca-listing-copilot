#!/usr/bin/env node

import { build } from "esbuild";
import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(fileURLToPath(new URL("..", import.meta.url)));
const entryPoint = resolve(root, "lib/listing/client/listing-copilot-sdk.mjs");
const outputPath = resolve(root, "app/listing-copilot-sdk.mjs");
const checkOnly = process.argv.includes("--check");

const result = await build({
  entryPoints: [entryPoint],
  bundle: true,
  write: false,
  format: "esm",
  platform: "browser",
  target: "es2022",
  treeShaking: true,
  legalComments: "none",
  banner: { js: "// Generated by scripts/build-public-browser-sdk.mjs. Do not edit directly." }
});

const generated = result.outputFiles?.[0]?.text || "";
if (!generated) throw new Error("public_browser_sdk_bundle_empty");

if (checkOnly) {
  const current = await readFile(outputPath, "utf8").catch(() => "");
  if (current !== generated) throw new Error("public_browser_sdk_bundle_stale");
  console.log("public browser SDK bundle is current");
} else {
  await writeFile(outputPath, generated);
  console.log("public browser SDK bundle built");
}
