#!/usr/bin/env node

import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

const sources = Object.freeze([
  Object.freeze({
    cohort: "INTERNAL_REVIEWED_GT",
    url: new URL("../data/catalog/vector-seed/feedback-writer-gt-seed-dataset.json", import.meta.url)
  }),
  Object.freeze({
    cohort: "EBAY_COLD_START",
    url: new URL("../data/eval/ebay-reference/ebay-c100-cloud-eval-dataset-20260707.json", import.meta.url)
  })
]);
const outputUrl = new URL(
  "../lib/listing/evaluation/launch-gate-image-source-index.generated.mjs",
  import.meta.url
);
const allowedBucket = "listing-feedback-images";
const allowedPathPrefixes = Object.freeze(["feedback/", "listing-assets/"]);

function cleanText(value) {
  return String(value || "").trim();
}

function datasetItems(dataset = {}) {
  if (Array.isArray(dataset)) return dataset;
  return dataset.items || dataset.records || dataset.results || [];
}

function sanitizeImage(image = {}) {
  const bucket = cleanText(image.bucket);
  const objectPath = cleanText(image.object_path || image.objectPath);
  if (
    bucket !== allowedBucket
    || !objectPath
    || objectPath.startsWith("/")
    || objectPath.includes("..")
    || !allowedPathPrefixes.some((prefix) => objectPath.startsWith(prefix))
  ) {
    throw new Error(`launch_gate_image_reference_out_of_scope:${objectPath || "missing"}`);
  }
  return {
    image_id: cleanText(image.image_id || image.id),
    bucket,
    object_path: objectPath,
    role: cleanText(image.role),
    content_sha256: cleanText(image.content_sha256 || image.contentSha256).toLowerCase() || null
  };
}

async function generateModule() {
  const records = [];
  const seen = new Set();
  const sourceDigests = {};
  for (const source of sources) {
    const sourceText = await readFile(source.url, "utf8");
    sourceDigests[source.cohort] = createHash("sha256").update(sourceText).digest("hex");
    const dataset = JSON.parse(sourceText);
    for (const item of datasetItems(dataset)) {
      const sourceFeedbackId = cleanText(item.source_feedback_id);
      if (!sourceFeedbackId || seen.has(sourceFeedbackId)) continue;
      const images = (Array.isArray(item.images) ? item.images : []).map(sanitizeImage);
      if (!images.length) continue;
      seen.add(sourceFeedbackId);
      records.push({
        source_feedback_id: sourceFeedbackId,
        evaluation_cohort: source.cohort,
        images
      });
    }
  }
  records.sort((left, right) => left.source_feedback_id.localeCompare(right.source_feedback_id));
  const payload = `${[
    "// Generated by scripts/generate-launch-gate-image-source-index.mjs.",
    "// Contains storage references only. Do not add titles, labels, or ground truth.",
    `export const launchGateImageSourceMetadata = Object.freeze(${JSON.stringify({
      record_count: records.length,
      source_sha256: sourceDigests
    }, null, 2)});`,
    `export const launchGateImageSourceRecords = Object.freeze(${JSON.stringify(records, null, 2)});`,
    ""
  ].join("\n")}`;
  return payload;
}

const expected = await generateModule();
if (process.argv.includes("--check")) {
  const current = await readFile(outputUrl, "utf8").catch(() => "");
  if (current !== expected) {
    console.error(`launch gate image source index is stale: ${fileURLToPath(outputUrl)}`);
    process.exitCode = 1;
  } else {
    console.log("launch gate image source index is current");
  }
} else {
  await writeFile(outputUrl, expected, { mode: 0o644 });
  console.log(`wrote ${fileURLToPath(outputUrl)}`);
}
