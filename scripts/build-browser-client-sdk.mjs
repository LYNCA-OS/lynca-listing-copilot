#!/usr/bin/env node

import assert from "node:assert/strict";
import { mkdtemp, readFile, rm } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { build } from "esbuild";

const source = "lib/listing/client/listing-copilot-sdk.mjs";
const output = "app/listing-copilot-sdk.mjs";
const checkOnly = process.argv.includes("--check");
const temporaryDirectory = checkOnly
  ? await mkdtemp(join(tmpdir(), "lynca-browser-sdk-check-"))
  : null;
const outputFile = temporaryDirectory ? join(temporaryDirectory, "listing-copilot-sdk.mjs") : output;

try {
  await build({
    entryPoints: [source],
    outfile: outputFile,
    bundle: true,
    format: "esm",
    platform: "browser",
    target: "es2022",
    treeShaking: true,
    minify: false,
    sourcemap: false,
    legalComments: "none",
    banner: {
      js: "// Generated by scripts/build-browser-client-sdk.mjs. Do not edit directly."
    }
  });

  if (checkOnly) {
    const [expected, actual] = await Promise.all([
      readFile(output, "utf8"),
      readFile(outputFile, "utf8")
    ]);
    assert.equal(expected, actual, `${output} is stale; run npm run build:browser-sdk`);
  }

  const bundled = await readFile(outputFile, "utf8");
  assert.equal(/(?:from\s+|import\s*\()["']\/?lib\//.test(bundled), false, "browser SDK must not import private /lib paths");
  console.log(checkOnly ? "Browser SDK bundle is current" : `Built ${output}`);
} finally {
  if (temporaryDirectory) await rm(temporaryDirectory, { recursive: true, force: true });
}
